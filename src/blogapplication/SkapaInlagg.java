    private void btnBifogaFilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBifogaFilActionPerformed
        JFileChooser fc = new JFileChooser();
        //FileSystemView.getFileSystemView().getHomeDirectory() (Gets desktop url)
        //"C:\\Users\\HHHLO\\OneDrive\\Documents"
        fc.setDialogTitle("Välj en fil att bifoga");
        FileNameExtensionFilter filterT = new FileNameExtensionFilter("TEXT FILES", "txt", "text");
        FileNameExtensionFilter filterB = new FileNameExtensionFilter("IMAGE FILES", "jpg", "jpeg", "png");
        fc.setFileFilter(filterT);
        fc.addChoosableFileFilter(filterB);
        int returnValue = fc.showSaveDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File f = fc.getSelectedFile();
            filnamn = f.getAbsolutePath();
            File fil = new File(filnamn);
            this.fil = fil;
            tfFilUrl.setText(filnamn);
            tfFilNamn.setEditable(true);

            lblFilUrl.setText("Förhandsgranska vald fil");
            lblFilUrl.setForeground(Color.BLUE.darker());
            lblFilUrl.setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));

        }

    }//GEN-LAST:event_btnBifogaFilActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        LocalDateTime nu = LocalDateTime.now();
        String datum = nu.toString().substring(0, 10);
        String tid = nu.toString().substring(11, 16);

        if (jTInlagg.getText().equals(null)) {
            JOptionPane.showMessageDialog(null, "Vänligen skriv ett inlägg först!");
        } else {
            int inlaggID = dataHanterare.getNextInlaggId();
            int skribent = userId;
            String inlagg = dataHanterare.stringFormat(jTInlagg.getText());

            int bsektion = 0;
            String vald = cmbBS.getSelectedItem().toString();
            if (vald.equalsIgnoreCase("Formella bloggen")) {
                bsektion = 1;
            } else if (vald.equalsIgnoreCase("Informella bloggen")) {
                bsektion = 2;
            } else if (vald.equalsIgnoreCase("Utbildningsbloggen")) {
                bsektion = 3;
            } else if (vald.equalsIgnoreCase("Forskningsbloggen")) {
                bsektion = 4;
            }

            String titel = tfTitel.getText();
            if (titel.equals("")) {
                titel = "Unnamed post";
            }
            titel = dataHanterare.stringFormat(titel);

            if (dataHanterare.skapaInlagg(inlaggID, skribent, inlagg, bsektion, titel, datum, tid)) {
                if (!tfFilUrl.getText().equals("")) {
                    cmbFK.setSelectedItem(5);
                    String filNamn = "Utan kategori";
                    if (!tfFilNamn.equals(null)) {
                        filNamn = dataHanterare.stringFormat(tfFilNamn.getText());
                    }
                    String fknamn = "Utan kategori";
                    String fkid = "5";
                    try {
                        fknamn = cmbFK.getSelectedItem().toString();
                        fkid = dataHanterare.getFKID(fknamn);
                    } catch (java.lang.NullPointerException e) {
                        fkid = "5";
                        System.out.println("Skapa inlägg error:" + e.getMessage());
                    }

                    dataHanterare.laggTillFil(filNamn, filnamn, inlaggID, fkid);
                }
                JOptionPane.showMessageDialog(null, "Ditt inlägg har skapats!");
                jTInlagg.setText("");
                tfTitel.setText("");
                tfFilUrl.setText("");
                tfFilNamn.setText("");
                filnamn = null;
                lblFilUrl.setText("");
                this.dispose();

                try {
                    int bloggsektionId = bsektion;
                    ArrayList<String> anvIdNotis = dataHanterare.getBloggNotisLista(bloggsektionId);
                    for (String s : anvIdNotis) {
                        int anvid = Integer.parseInt(s);
                        dataHanterare.skickaBloggNotis(anvid, userId, bloggsektionId);
                    }
                } catch (java.lang.NullPointerException e) {
                    System.out.print("Inga användare vill ha notiser från denna blogg");
                }

            } else {
                System.out.print("Misslyckades112");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

